// O que é uma API?

// Application Programming interface

// API é uma interface de comunicação entre sistemas.

// Uma maneira de se comunicar via HTTP a um servidor e fazer operações como: visualizar, deletar, criar e atualizar dados

// CRUD (create, read, update, delete)

// Grandes aplicações como facebook e instagram utilizam APIs

// Grande parte dos softwares desenvolvidos em node.js tem uma API

// E utilizam um framwork chamado Express para isso

// Uma forma de se comunicar http com o servidor de uma forma que o codigo do frontend fique separado do backend, separando a lógica
